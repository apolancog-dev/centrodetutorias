-- 1. Tabla de Perfiles Públicos (Extiende auth.users)
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS public.users (
    id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
    full_name TEXT NOT NULL,
    email TEXT UNIQUE NOT NULL,
    phone TEXT,
    role TEXT DEFAULT 'free_user' CHECK (role IN ('free_user', 'subscriber', 'admin')),
    subscription_status TEXT DEFAULT 'inactive' CHECK (subscription_status IN ('active', 'inactive', 'trial', 'cancelled', 'expired')),
    academic_level TEXT,
    main_interest TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    last_login TIMESTAMP WITH TIME ZONE
);

-- Habilitar Seguridad a Nivel de Fila (RLS) en la tabla users
ALTER TABLE public.users ENABLE ROW LEVEL SECURITY;

-- -----------------------------------------------------
-- 1.1 Funciones Auxiliares de Seguridad (Evitan recursión infinita en RLS)
-- -----------------------------------------------------
CREATE OR REPLACE FUNCTION public.is_admin(user_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.users
    WHERE id = user_id AND role = 'admin'
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.is_subscriber(user_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.users
    WHERE id = user_id AND role = 'subscriber'
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.has_premium_access(user_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.users
    WHERE id = user_id AND (role = 'subscriber' OR role = 'admin')
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Políticas RLS para users
CREATE POLICY "Permitir a los usuarios ver su propio perfil" 
    ON public.users FOR SELECT 
    USING (auth.uid() = id);

CREATE POLICY "Permitir a los usuarios actualizar su propio perfil" 
    ON public.users FOR UPDATE 
    USING (auth.uid() = id);

CREATE POLICY "Permitir a los administradores ver todos los perfiles" 
    ON public.users FOR SELECT 
    USING (public.is_admin(auth.uid()));

CREATE POLICY "Permitir a los administradores actualizar cualquier perfil" 
    ON public.users FOR UPDATE 
    USING (public.is_admin(auth.uid()));

-- -----------------------------------------------------
-- 2. Tabla de Categorías
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS public.categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    slug TEXT UNIQUE NOT NULL,
    description TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Habilitar RLS en categories
ALTER TABLE public.categories ENABLE ROW LEVEL SECURITY;

-- Políticas RLS para categories
CREATE POLICY "Permitir a cualquiera ver categorías" 
    ON public.categories FOR SELECT 
    USING (true);

CREATE POLICY "Permitir solo a administradores modificar categorías" 
    ON public.categories FOR ALL 
    USING (public.is_admin(auth.uid()));

-- -----------------------------------------------------
-- 3. Tabla de Contenidos (Cursos, Artículos, Ejercicios)
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS public.content_items (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    slug TEXT UNIQUE NOT NULL,
    description TEXT,
    category_id BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
    academic_level TEXT, -- Primaria, Secundaria, Universidad, etc.
    content_type TEXT NOT NULL CHECK (content_type IN ('video', 'pdf', 'article', 'exercise', 'download', 'course', 'lesson')),
    access_type TEXT NOT NULL DEFAULT 'free' CHECK (access_type IN ('free', 'premium', 'subscriber_only')),
    thumbnail_url TEXT,
    video_url TEXT,
    document_url TEXT,
    body_content TEXT,
    status TEXT NOT NULL DEFAULT 'active' CHECK (status IN ('active', 'inactive')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Habilitar RLS en content_items
ALTER TABLE public.content_items ENABLE ROW LEVEL SECURITY;

-- Políticas RLS para content_items
CREATE POLICY "Permitir a usuarios ver contenidos libres o sus propios accesos" 
    ON public.content_items FOR SELECT 
    USING (
        access_type = 'free' 
        OR public.has_premium_access(auth.uid())
    );

CREATE POLICY "Permitir a administradores realizar todo en content_items" 
    ON public.content_items FOR ALL 
    USING (public.is_admin(auth.uid()));

